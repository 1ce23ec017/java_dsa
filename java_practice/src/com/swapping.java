package com;

public class swapping {
	public static void main(String[] args) {
		int a = 4;
		int b= 5;
		int temp;
		System.out.println("the value of a " +a);
		System.out.println("the value of b  " +b);
		
		temp = a;
		a = b;
		b = temp ;
		System.out.println("the value of a :"+a);
		System.out.println("the value of temp: " +temp);
	
		
	}

}
